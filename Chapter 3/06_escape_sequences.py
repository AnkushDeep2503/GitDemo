medicines = "take 2 tablets in the morning. \n2 at night"  # \n - moves the string to the next line in result
print(medicines)

message = "sleep as soon\tas possible" # \t - backslash + t is used for inserting tab/space in the result
# print(len(message))
print(message)

greetings = "warm wishes to your friend\'s"  # \' - moves the string to the next line in result
print(greetings)

disclaimer = "dont go beyo\\nd the wall" # \\ - used for single backslash 
print(disclaimer)