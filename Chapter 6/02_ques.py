# Write a program to print yes when the age enetered by the user is greater than or equal to 18.

age = int(input("Enter the age: "))

if(age>=18):
        print("yes")
else:
        print("no")

# Relational Operators:
# a == 18 (equals to)
# a >=18 (greater than equal to)
# a<=18 (lesser than equal to)
