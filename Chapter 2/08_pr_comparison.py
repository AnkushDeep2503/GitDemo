# Question: Use comparison operators to find out whether a given variable a is greater than 'b' or not. Take a = 34 and b = 80.

a = 34
b = 80

print ("a is greater than b or not: ", a>b)
print ("a is equal to b or not: ", a==b)
print ("a is lesser than b or not: ", a<b)
print ("a is greater than or equal to b or not: ", a>=b)
print ("a is not equal to b or not: ", a!=b)