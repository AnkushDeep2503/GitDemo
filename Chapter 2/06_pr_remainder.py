# Question: Write a python program to find remainder when a number is divided by 2.
# We have to use modulus operator for this kind of question using sign '%', for example a%b to find the remainder.

a = 23
b = 10
print ("The remainder when a is divided by b is ", (a%b))

c = 36
d = 3
